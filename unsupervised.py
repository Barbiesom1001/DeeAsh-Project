from sklearn.cluster import KMeans
from sklearn.metrics import silhouette_score

print("Elbow Method & Silhouette Score\n")

inertia_list = []
sil_list     = []
k_range      = range(2, 8)

for k in k_range:
    km     = KMeans(n_clusters=k, random_state=42, n_init=10)
    labels = km.fit_predict(X_scaled)
    inertia_list.append(km.inertia_)
    sil_list.append(silhouette_score(X_scaled, labels))

fig, axes = plt.subplots(1, 2, figsize=(14, 5))

# Elbow Plot
axes[0].plot(list(k_range), inertia_list, 'bo-', linewidth=2, markersize=8)
axes[0].set_xlabel('จำนวน Cluster (K)', fontsize=13)
axes[0].set_ylabel('Inertia (Within-cluster Sum of Squares)', fontsize=13)
axes[0].set_title('Elbow Method — หาจุด "ข้อศอก"', fontsize=15)
axes[0].grid(True, alpha=0.3)

# Silhouette Score Plot
axes[1].bar(list(k_range), sil_list, color='steelblue', alpha=0.8)
axes[1].set_xlabel('จำนวน Cluster (K)', fontsize=13)
axes[1].set_ylabel('Silhouette Score', fontsize=13)
axes[1].set_title('Silhouette Score — ยิ่งสูงยิ่งดี', fontsize=15)
axes[1].grid(True, alpha=0.3, axis='y')

plt.tight_layout()
plt.show()

