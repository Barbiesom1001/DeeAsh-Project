def label_target(text):
    """
    Rule-Based Label:
      1 = Winning Zone  → ต้องการทั้งด้านอ่อนโยนและสีติดทน
      0 = Non-Target    → ระบุด้านเดียว หรือเน้นปัจจัยอื่น (ราคา/ความสะดวก)
    """
    text = str(text)
    needs_gentle = any(word in text for word in gentle_keys)
    needs_color  = any(word in text for word in color_keys)
    return 1 if (needs_gentle and needs_color) else 0

